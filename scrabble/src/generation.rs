pub fn generate_board() -> [[Tile; BOARD_SIZE]; BOARD_SIZE] {
    let char_board: [[char; BOARD_SIZE]; BOARD_SIZE] = [
        ['♠','-','-','♡','-','-','-','♠','-','-','-','♡','-','-','♠',],
        ['-','♣','-','-','-','♢','-','-','-','♢','-','-','-','♣','-',],
        ['-','-','♣','-','-','-','♡','-','♡','-','-','-','♣','-','-',],
        ['♡','-','-','♣','-','-','-','♡','-','-','-','♣','-','-','♡',],
        ['-','-','-','-','♣','-','-','-','-','-','♣','-','-','-','-',],
        ['-','♢','-','-','-','♢','-','-','-','♢','-','-','-','♢','-',],
        ['-','-','♡','-','-','-','♡','-','♡','-','-','-','♡','-','-',],
        ['♠','-','-','♡','-','-','-','★','-','-','-','♡','-','-','♠',],
        ['-','-','♡','-','-','-','♡','-','♡','-','-','-','♡','-','-',],
        ['-','♢','-','-','-','♢','-','-','-','♢','-','-','-','♢','-',],
        ['-','-','-','-','♣','-','-','-','-','-','♣','-','-','-','-',],
        ['♡','-','-','♣','-','-','-','♡','-','-','-','♣','-','-','♡',],
        ['-','-','♣','-','-','-','♡','-','♡','-','-','-','♣','-','-',],
        ['-','♣','-','-','-','♢','-','-','-','♢','-','-','-','♣','-',],
        ['♠','-','-','♡','-','-','-','♠','-','-','-','♡','-','-','♠',]
    ];

    let mut board = [[Tile::new('-'); 15]; 15];

    for i in 0..BOARD_SIZE {
        for j in 0..BOARD_SIZE {
            board[i][j] = Tile::new(char_board[i][j]);
        }
    }
    board
}

